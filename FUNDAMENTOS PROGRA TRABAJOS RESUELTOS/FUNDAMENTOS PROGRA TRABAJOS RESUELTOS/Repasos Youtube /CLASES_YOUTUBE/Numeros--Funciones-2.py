import math # math es un modulo que se agrega para que python haga cosas matematicas


print(round(1.3)) #round devuelve el numero mas cercano a redondear
print(round(1.5)) 
print(abs(-77)) #entraga el valor absoluto (saca el signo si es que es negativo por ej)

print(math.ceil(1.1)) #entrega el numero entero mas cercano de arriba
print(math.floor(1.1)) #entrega el numero entero mas cercano de abajo
print(math.floor(77)) #devuelve si el numero corresponde que no es un numero
print(math.floor("77")) 
print(math.pow(10, 3)) #potencia
print(math.sqrt(9)) #raiz cuadrada